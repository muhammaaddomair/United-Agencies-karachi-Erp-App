function getInvoicePdfPayload(invoiceId) {
  const db = openDatabase();

  try {
    const invoice = db.prepare(`
      SELECT
        id,
        invoice_no,
        bill_no,
        delivery_challan_no,
        order_contract_no,
        order_date,
        inspection_note_no,
        invoice_date,
        to_text
      FROM invoices
      WHERE id = ?
    `).get(invoiceId);

    if (!invoice) {
      throw new Error('Invoice not found.');
    }

    const items = db.prepare(`
      SELECT
        ii.quantity,
        ii.unit_price,
        ii.product_description,
        inv.reg_no,
        inv.batch_no,
        inv.mfg_date,
        inv.exp_date
      FROM invoice_items ii
      LEFT JOIN inventory_items inv ON inv.id = ii.inventory_item_id
      WHERE ii.invoice_id = ?
      ORDER BY ii.id ASC
    `).all(invoiceId);

      return {
        fileNo: invoice.invoice_no || String(invoice.id),
        billNo: invoice.bill_no || '',
        outputDir: getInvoicesDir(),
        deliveryChallanNo: invoice.delivery_challan_no || '',
      orderContractNo: invoice.order_contract_no || '',
      orderDate: invoice.order_date || '',
      inspectionNoteNo: invoice.inspection_note_no || '',
        invoiceDate: invoice.invoice_date || '',
        toText: invoice.to_text || '',
        products: items.map((item) => ({
        regNo: item.reg_no || '',
        batchNo: item.batch_no || '',
        mfgDate: item.mfg_date || '',
        expDate: item.exp_date || '',
        productDescription: item.product_description || '',
        quantity: item.quantity,
        unitPrice: item.unit_price,
      })),
    };
  } finally {
    db.close();
  }
}

