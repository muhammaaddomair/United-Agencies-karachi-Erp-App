function saveOrder(payload) {
  const institutionName = String(payload.institutionName || '').trim();
  const orderNumber = String(payload.orderNumber || '').trim();
  const orderDate = String(payload.orderDate || '').trim();
  const sourceFilePath = String(payload.sourceFilePath || '').trim();

  if (!institutionName || !orderNumber || !orderDate || !sourceFilePath) {
    throw new Error('Institution name, order number, order date, and file are required.');
  }

  if (!fs.existsSync(sourceFilePath)) {
    throw new Error('Selected file could not be found.');
  }

  const ext = path.extname(sourceFilePath).toLowerCase();
  const allowedExtensions = new Set(['.pdf', '.png', '.jpg', '.jpeg', '.webp', '.bmp']);
  if (!allowedExtensions.has(ext)) {
    throw new Error('Only PDF and image files are allowed.');
  }

  const originalName = path.basename(sourceFilePath);
  const safeBase = originalName.replace(/[^\w.-]+/g, '_');
  const targetName = `${Date.now()}-${safeBase}`;
  const targetPath = path.join(getOrdersDir(), targetName);
  fs.copyFileSync(sourceFilePath, targetPath);

  const db = openDatabase();
  try {
    const createdAt = new Date().toISOString();
    const result = db.prepare(`
        INSERT INTO orders (
          institution_name,
          order_number,
          order_date,
          file_name,
          file_path,
          created_at
        )
        VALUES (?, ?, ?, ?, ?, ?)
      `).run(
        institutionName,
        orderNumber,
        orderDate,
        originalName,
        targetPath,
        createdAt
    );

    return {
      ok: true,
      orderId: Number(result.lastInsertRowid),
      filePath: targetPath,
    };
  } finally {
    db.close();
  }
}

function updatePaymentStatus(payload) {
  const paymentId = Number(payload.paymentId);
  const status = String(payload.status || '').trim().toLowerCase();

  if (!paymentId) {
    throw new Error('Payment ID is required.');
  }

  if (!['pending', 'received'].includes(status)) {
    throw new Error('Invalid payment status.');
  }

  const db = openDatabase();
  try {
    db.prepare(`
      UPDATE payments
      SET status = ?
      WHERE id = ?
    `).run(status, paymentId);
    return { ok: true };
  } finally {
    db.close();
  }
}

async function generateInvoicePdf(invoicePayload) {
  const helperPath = path.join(__dirname, 'backend', 'invoice_pdf.py');
  const tempPayloadPath = path.join(getAppDataDir(), `invoice-payload-${Date.now()}.json`);

  fs.writeFileSync(tempPayloadPath, JSON.stringify(invoicePayload, null, 2), 'utf8');

  try {
    return await runPythonScript(helperPath, [tempPayloadPath]);
  } finally {
    fs.rmSync(tempPayloadPath, { force: true });
  }
}

async function saveInvoice(payload) {
  if (!Array.isArray(payload.products) || payload.products.length === 0) {
    throw new Error('Please select at least one product.');
  }

  const db = openDatabase();

  try {
    db.exec('BEGIN TRANSACTION');

    for (const item of payload.products) {
      const row = db.prepare('SELECT quantity FROM inventory_items WHERE id = ?').get(item.inventoryItemId);
      if (!row) {
        throw new Error('A selected product no longer exists in inventory.');
      }
      const available = Number(row.quantity || 0);
      const qty = Number(item.quantity || 0);
      if (qty <= 0) {
        throw new Error('Selected product quantity must be greater than zero.');
      }
      if (qty > available) {
        throw new Error(`Insufficient stock for product ID ${item.inventoryItemId}. Available: ${available.toFixed(2)}`);
      }
    }

      const createdAt = new Date().toISOString();
      const invoiceDate = payload.invoiceDate || createdAt.slice(0, 10);
      const toText = (payload.toText || '').trim();
    const totalQty = payload.products.reduce((sum, item) => sum + Number(item.quantity || 0), 0);
    const customerName = toText ? toText.split(/\r?\n/)[0] : '';
    const serviceType = payload.products.map((item) => item.productDescription || '').join(', ');

    const result = db.prepare(`
      INSERT INTO invoices (
        invoice_no, customer_name, total, created_at, service_type, phone, paid_amount,
        payment_mode, str_no, ntn_no, bill_no, delivery_challan_no, order_contract_no,
        order_date, inspection_note_no, invoice_date, to_text
      )
      VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
      `).run(
        payload.fileNo,
        customerName,
        totalQty,
      createdAt,
      serviceType,
      '',
      0,
      '',
        '',
        '',
        payload.billNo,
        payload.deliveryChallanNo,
        payload.orderContractNo,
        payload.orderDate,
        payload.inspectionNoteNo,
        invoiceDate,
        toText
      );

    for (const item of payload.products) {
      db.prepare(`
        INSERT INTO invoice_items (
          invoice_id, inventory_item_id, product_description, quantity, unit_price
        )
        VALUES (?, ?, ?, ?, ?)
      `).run(
        result.lastInsertRowid,
        item.inventoryItemId,
        item.productDescription,
        item.quantity,
        item.unitPrice
      );

      db.prepare('UPDATE inventory_items SET quantity = quantity - ? WHERE id = ?').run(
        item.quantity,
        item.inventoryItemId
      );
    }

    db.prepare(`
      INSERT OR IGNORE INTO payments (
        invoice_id,
        bill_no,
        institution_name,
        invoice_date,
        status,
        created_at
      )
      VALUES (?, ?, ?, ?, ?, ?)
      `).run(
        result.lastInsertRowid,
        payload.billNo,
        customerName,
        invoiceDate,
        'pending',
        createdAt
      );

    db.exec('COMMIT');

    const inventorySnapshot = new Map(
      payload.products.map((item) => {
        const inventoryRow = db.prepare(`
          SELECT reg_no, batch_no, mfg_date, exp_date, product_description
          FROM inventory_items
          WHERE id = ?
        `).get(item.inventoryItemId);

        return [item.inventoryItemId, inventoryRow || {}];
      })
    );

      const pdfPayload = {
        fileNo: payload.fileNo || String(result.lastInsertRowid),
        billNo: payload.billNo,
        outputDir: getInvoicesDir(),
        deliveryChallanNo: payload.deliveryChallanNo,
        orderContractNo: payload.orderContractNo,
        orderDate: payload.orderDate,
        inspectionNoteNo: payload.inspectionNoteNo,
        invoiceDate,
        toText,
        products: payload.products.map((item) => {
        const inventoryItem = inventorySnapshot.get(item.inventoryItemId) || {};
        return {
          regNo: inventoryItem.reg_no || '',
          batchNo: inventoryItem.batch_no || '',
          mfgDate: inventoryItem.mfg_date || '',
          expDate: inventoryItem.exp_date || '',
          productDescription: inventoryItem.product_description || item.productDescription || '',
          quantity: item.quantity,
          unitPrice: item.unitPrice,
        };
      }),
    };

    try {
      const pdfResult = await generateInvoicePdf(pdfPayload);
      return {
        ok: true,
        invoiceId: Number(result.lastInsertRowid),
        pdfPath: pdfResult.path || '',
      };
    } catch (pdfError) {
      return {
        ok: true,
        invoiceId: Number(result.lastInsertRowid),
        pdfPath: '',
        pdfError: pdfError.message,
      };
    }
  } catch (error) {
    db.exec('ROLLBACK');
    throw error;
  } finally {
    db.close();
  }
}

async function generateInvoicePdfForInvoice(invoiceId) {
  const pdfPayload = getInvoicePdfPayload(invoiceId);
  const pdfResult = await generateInvoicePdf(pdfPayload);
  return {
    ok: true,
    pdfPath: pdfResult.path || '',
  };
}

