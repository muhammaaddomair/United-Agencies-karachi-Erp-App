function ensureSchema() {
  const db = openDatabase();
  db.exec(`
    CREATE TABLE IF NOT EXISTS invoices (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      invoice_no TEXT,
      customer_name TEXT,
      total REAL,
      created_at TEXT,
      service_type TEXT,
      phone TEXT,
      paid_amount REAL,
      payment_mode TEXT,
      str_no TEXT,
      ntn_no TEXT,
      bill_no TEXT,
      delivery_challan_no TEXT,
      order_contract_no TEXT,
      order_date TEXT,
      inspection_note_no TEXT,
      invoice_date TEXT,
      to_text TEXT
    );

    CREATE TABLE IF NOT EXISTS inventory_items (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      reg_no TEXT,
      batch_no TEXT,
      mfg_date TEXT,
      exp_date TEXT,
      quantity REAL,
      product_description TEXT,
      trade_price REAL,
      discount_percent REAL,
      price_per_unit REAL,
      amount_pkr REAL,
      created_at TEXT
    );

    CREATE TABLE IF NOT EXISTS invoice_items (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      invoice_id INTEGER,
      inventory_item_id INTEGER,
      product_description TEXT,
      quantity REAL,
      unit_price REAL
    );

    CREATE TABLE IF NOT EXISTS orders (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      institution_name TEXT,
      order_number TEXT,
      order_date TEXT,
      file_name TEXT,
      file_path TEXT,
      created_at TEXT
    );

    CREATE TABLE IF NOT EXISTS payments (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      invoice_id INTEGER UNIQUE,
      bill_no TEXT,
      institution_name TEXT,
      invoice_date TEXT,
      status TEXT DEFAULT 'pending',
      created_at TEXT
    );
  `);

  const orderColumns = db.prepare('PRAGMA table_info(orders)').all();
  const orderColumnNames = new Set(orderColumns.map((column) => column.name));
  if (!orderColumnNames.has('order_date')) {
    db.exec('ALTER TABLE orders ADD COLUMN order_date TEXT');
  }

  db.exec(`
    INSERT OR IGNORE INTO payments (
      invoice_id,
      bill_no,
      institution_name,
      invoice_date,
      status,
      created_at
    )
    SELECT
      invoices.id,
      invoices.bill_no,
      invoices.customer_name,
      invoices.invoice_date,
      'pending',
      invoices.created_at
    FROM invoices
    WHERE invoices.id IS NOT NULL
  `);
  db.close();
}

