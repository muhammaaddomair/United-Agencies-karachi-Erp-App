function parseJsonFile(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
  } catch (_error) {
    return null;
  }
}

function getPostgresConfig() {
  if (process.env.POSTGRES_URL) {
    return {
      connectionString: process.env.POSTGRES_URL,
    };
  }

  const candidatePaths = [
    path.join(__dirname, 'db.config.json'),
    getDbConfigPath(),
  ];

  for (const candidatePath of candidatePaths) {
    if (!fs.existsSync(candidatePath)) continue;
    const config = parseJsonFile(candidatePath);
    if (!config) continue;

    if (config.connectionString) {
      return {
        connectionString: config.connectionString,
      };
    }

    if (config.host && config.user && config.database) {
      return {
        host: config.host,
        port: Number(config.port || 5432),
        user: config.user,
        password: config.password || '',
        database: config.database,
        ssl: config.ssl ? { rejectUnauthorized: false } : false,
      };
    }
  }

  return null;
}

function isPostgresConfigured() {
  return !!getPostgresConfig();
}

let pgPool = null;

function getPgPool() {
  if (pgPool) {
    return pgPool;
  }

  const config = getPostgresConfig();
  if (!config) {
    throw new Error(
      `PostgreSQL is not configured. Create ${getDbConfigPath()} with your office database settings or set POSTGRES_URL.`
    );
  }

  pgPool = new Pool({
    ...config,
    max: 10,
  });
  return pgPool;
}

function toIsoTimestamp(value) {
  if (!value) return '';
  if (value instanceof Date) return value.toISOString();
  const parsed = new Date(value);
  return Number.isNaN(parsed.getTime()) ? String(value) : parsed.toISOString();
}

function toIsoDate(value) {
  if (!value) return '';
  if (value instanceof Date) return value.toISOString().slice(0, 10);
  const parsed = new Date(value);
  return Number.isNaN(parsed.getTime()) ? String(value).slice(0, 10) : parsed.toISOString().slice(0, 10);
}

