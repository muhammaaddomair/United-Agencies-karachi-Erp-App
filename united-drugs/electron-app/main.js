function runPythonHelper(args) {
  const helperPath = path.join(__dirname, 'backend', 'auth_helper.py');

  return new Promise((resolve, reject) => {
    const child = spawn(getPythonPath(), [helperPath, ...args], {
      cwd: path.join(__dirname, '..'),
      windowsHide: true,
    });

    let stdout = '';
    let stderr = '';

    child.stdout.on('data', (chunk) => {
      stdout += chunk.toString();
    });

    child.stderr.on('data', (chunk) => {
      stderr += chunk.toString();
    });

    child.on('error', reject);
    child.on('close', (code) => {
      if (code !== 0) {
        reject(new Error(stderr.trim() || `Python helper exited with code ${code}`));
        return;
      }

      try {
        resolve(JSON.parse(stdout || '{}'));
      } catch (_error) {
        reject(new Error(`Invalid helper response: ${stdout}`));
      }
    });
  });
}

